		<p><strong>WineObs</strong> es una empresa joven creada en 2014 por Carolina Puebla, relacionista pública y guía de turismo; y Sofía Ruiz Cavanagh, Licenciada en Enología.
			Siendo ambas originarias de la ciudad de Mendoza y muy interesadas en la vitivinicultura y el turismo regional, nuestra idea es ofrecer una forma diferente de obsesionarnos con el vino y las bodegas.
Con el objetivo de facilitar a los amantes del vino la accesibilidad a degustaciones, visitas y otras actividades relacionadas al enoturismo, con <strong>WineObs</strong> proponemos un sitio web que reúne toda la información de relevancia turística de un gran número de bodegas. De esta manera, los visitantes podrán planificar de forma simple y rápida un extraordinario día de degustaciones y visitas turísticas.
Lo que distingue a <strong>WineObs</strong> es que ofrecemos reservas en tiempo real al mismo precio al que se encuentran los mismos servicios en cada una de las diferentes bodegas.
Por otra parte, el sitio también funciona como un nuevo canal de comunicación que las bodegas pueden utilizar para ofrecer y promocionar sus servicios, contribuyendo tanto a la diversificación de las bodegas visitadas, como al crecimiento y afianzamiento de la marca “vino argentino”.
